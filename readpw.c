static uint16_t crc16(uint8_t *buffer, uint16_t buffer_length)
{
    uint8_t crc_hi = 0xFF; /* high CRC byte initialized */
    uint8_t crc_lo = 0xFF; /* low CRC byte initialized */
    unsigned int i; /* will index into CRC lookup */

    /* pass through message buffer */
    while (buffer_length--) {
        i = crc_hi ^ *buffer++; /* calculate the CRC  */
        crc_hi = crc_lo ^ table_crc_hi[i];
        crc_lo = table_crc_lo[i];
    }

    return (crc_hi << 8 | crc_lo);
}

/* Builds a RTU request header */
static int modbus_rtu_req(uint8_t *buf, int *len, int slaveid, int function,
                                           int addr, int nb)
{
    assert(slaveid != -1);
    buf[0] = slaveid;
    buf[1] = function;
    buf[2] = addr >> 8;
    buf[3] = addr & 0x00ff;
    buf[4] = nb >> 8;
    buf[5] = nb & 0x00ff;
    uint16_t crc = crc16(buf, 6);
    buf[6] = crc >> 8;
    buf[7] = crc & 0x00FF;
    *len = 8;
    return 8;
}

int modbus_rtu_recv(int s, uint8_t *buf, int maxlen)
{
	int n;
	n = Read(s, buf, 3);
	assert(n==3);
	assert(buf[2]*2+5<maxlen);
	n = Read(s, buf+3, buf[2]+2);
	assert(n==buf[2]+2);
	n+=3;
	uint16_t crc_calculated;
        uint16_t crc_received;

       	crc_calculated = crc16(buf, n - 2);
       	crc_received = (buf[n - 2] << 8) | buf[n - 1];

      	if (crc_calculated == crc_received) 
        	return n;	
	assert("crc error");
}

void Process(char *server, char * port) 
{	
	uint8_t buf[MAXLEN];
	int r_fd;
	int n,i;
	int optval;
   	socklen_t optlen = sizeof(optval);
	FILE *fp;
	r_fd= Tcp_connect(server,port);
	optval = 1;
	Setsockopt(r_fd, SOL_SOCKET, SO_KEEPALIVE, &optval, optlen);
	optval = 3;
	Setsockopt(r_fd, SOL_TCP, TCP_KEEPCNT, &optval, optlen);
	optval = 2;
	Setsockopt(r_fd, SOL_TCP, TCP_KEEPIDLE, &optval, optlen);
	optval = 2;
	Setsockopt(r_fd, SOL_TCP, TCP_KEEPINTVL, &optval, optlen);


	while (1) {
//read temp
		modbus_rtu_req(buf, &n, 2, 4, 0, 2);
#ifdef DEBUG
		dump_pkt(buf,n);
#endif
		Write(r_fd, buf, n);
		n = modbus_rtu_recv(r_fd, buf, MAXLEN);
#ifdef DEBUG
		fprintf(stderr,"get %d\n",n);
		dump_pkt(buf,n);
#endif
		if(n!=9) {
#ifdef DEBUG
			fprintf(stderr,"error\n");
#endif
			exit(0);
		}
		if(n<=0)   {
			exit(0);
		}
		short int t;
		t = (buf[3]<<8) + buf[4];
#ifdef DEBUG
		fprintf(stderr,"Temp=%d ",t);
		fp=fopen("temp","r+");
		if(fp) {
			fseek(fp,0L,SEEK_SET);
			fprintf(fp,"%6.2f",t/10.0);
			fclose(fp);
		}
#endif
		t = (buf[5]<<8) + buf[6];
		fp=fopen("hum","r+");
		if(fp) {
			fseek(fp,0L,SEEK_SET);
			fprintf(fp,"%5.2f",t/10.0);
			fclose(fp);
		}
#ifdef DEBUG
		fprintf(stderr,"Hum=%d ",t);
		fprintf(stderr,"\n");
#endif

		modbus_rtu_req(buf, &n, 1, 3, 40400, 6);
#ifdef DEBUG
		dump_pkt(buf,8);
#endif
		Write(r_fd, buf, 8);
		n = modbus_rtu_recv(r_fd, buf, MAXLEN);
#ifdef DEBUG
		fprintf(stderr,"get %d\n",n);
		dump_pkt(buf,n);
#endif
		if(n!=17) {
#ifdef DEBUG
			fprintf(stderr,"error\n");
#endif
			exit(0);
		}
		if(n<=0)   {
			exit(0);
		}
#ifdef DEBUG
		for(i=0;i<6;i++) 
			fprintf(stderr,"L%d=%d ",i+1, (buf[3+i*2]<<8)+buf[4+i*2]);
		fprintf(stderr,"\n");
#endif
		for(i=0;i<6;i++) {
			char fname[MAXLEN];
			sprintf(fname,"L%d",i+1);
			fp=fopen(fname,"r+");
			if(fp) {
				fseek(fp,0L,SEEK_SET);
				if( (buf[3+i*2]<<8)+buf[4+i*2]==1)
					fprintf(fp,"1");
				else
					fprintf(fp,"0");
				fclose(fp);
			}
		}
		fp=fopen("lastrun","r+");
		if(fp) {
			fseek(fp,0L,SEEK_SET);
			fprintf(fp,"%ld",ctime(NULL));
			fclose(fp);
		}
		
		sleep(1);
	}
}


