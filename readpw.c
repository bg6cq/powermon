int main(int argc, char *argv[])
{
	char *server="202.38.64.142";
	char *port = "8000";
	signal(SIGCHLD,SIG_IGN);
	if(argc==3) {
		server=argv[1];
		port = argv[2];
	} else if(argc!=1) {
		usage();
		exit(0);
	}

#ifndef DEBUG
	daemon_init("readpw",LOG_DAEMON);
	while(1) {
                int pid;
                pid=fork();
                if(pid==0) // i am child, will do the job
                        break;
                else if(pid==-1) // error
                        exit(0);
                else
                        wait(NULL); // i am parent, wait for child
                sleep(2);  // if child exit, wait 2 second, and rerun
        }
#endif
	chdir("/usr/src/powermon");
	Process(server, port);
	return(0);
}
